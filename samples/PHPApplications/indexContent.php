<h3>Overview</h3>
<p>
  The <a href="http://msdn.microsoft.com/en-us/data/bb931106.aspx">WCF Data Services</a> framework consists of a
  combination of patterns and libraries that enable the creation and consumption of data services for the web.
  The goal of the WCF Data Services framework is to facilitate the creation of flexible data services that
  are naturally integrated with the web, using URIs to point to pieces of data and simple, well-known formats
  to represent that data, such as JSON and plain XML. This results in the data service being surfaced to the web
  as a REST-style resource collection that is addressable with URIs and that agents can interact with using the
  usual HTTP verbs such as GET, POST or DELETE.

  OData toolkit for PHP enables PHP developers to access data services created WCF
  Data Services framework. The goal is to provide the similar functionality as .Net library to access and modify
  the data and its relationship using URIs which points to pieces of data integrated with the web.<br/><br/>

  Please visit the <a href="http://odataphp.codeplex.com/">project website</a> for detail information.
</p>

